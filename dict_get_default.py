"""
Изменяемый объект как значение по умолчанию метода словаря get.
"""

dct = {1: 100, 2: 200, 3: 300, 4: 400, 5: 500, 6: 600, 7: 700, 8: 800, 9: 900, 10: [1, 2, 3, 4, 5]}

a = dct.get(1)
tmp = []
for i in (11, 12):
    tmp.append(dct.get(i, {9: 1, 99: 2, 999: 3}))
b, c = tmp

print(
    'a', a,
    '\nb', b,
    '\nc', c
)

b[9] = 10
c[99] = 100

print(
    'b', b,
    '\nc', c
)